"""
    打印9X9 乘法表
    while 双循环嵌套
"""
i = 1
while i <=9:                # 外层循环,行数控制
    j=1                     # 内层循环变量,列数控制
    while j <= i:
        print(f"{j}*{i}={j*i}\t",end='')        # 打印,并不换行
        j += 1                                  # 列数变量+1
    i += 1                                      # 行数+1
    print()                                     # 行进行切换