# 单引号定义方式
name = '努力，拼搏'
print(type(name))
print(name)

# 双引号定义法
name1 = "好好学习，不虚度好时光"
print(type(name1))
print(name1)

# 三引号定义法
name2 = """无悔青春，唯有拼搏和提升自己"""
print(type(name2))
print(name2)

# 字符串嵌套，单引号定义法:可以包含双引号
name3 = '"upper"'
print(name3)

# 字符串嵌套，双引号定义法:可以包含单引号
name4 = "'irony'"
print(name4)

# 字符串嵌套，使用转义字符
name5 = "\"每天都要开心，要有一颗上进的小心脏"
print(name5)